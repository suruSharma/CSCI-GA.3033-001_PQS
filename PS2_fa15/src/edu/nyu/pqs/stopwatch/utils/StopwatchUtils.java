package edu.nyu.pqs.stopwatch.utils;

/**
 * This class contains all enums and constants that are used in the code.
 * 
 */
public class StopwatchUtils {

  /**
   * enum for the various states of the stop watch
   * START : The stop watch has been reset
   * RUNNING : The stop watch is running
   * STOP : The stop watch has been stopped
   *
   */
  public static enum StopwatchState {
    START, RUNNING, STOP;
  }
}
